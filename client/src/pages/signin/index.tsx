import { PageTemplate } from "@/templates/PageTemplate";
import * as Yup from "yup";
import { useFormik } from "formik";
import Head from "next/head";
import styled, { css } from "styled-components";
import {
  HeaderSign,
  SignInWrapper,
  FormWrapper,
} from "../../styles/signin/index.style";
import React, { useState } from "react";
import Button from "@/components/Button/Button";
import { signinuser, clearError } from "@/redux/reducer/register";
import {
  errorValue,
  selectValue,
  loadingValue,
} from "@/redux/reducer/register";
import { paramsValue } from "@/redux/reducer/location";
import {
  geoValue,
  getRealEstateData,
  geolocateUser,
} from "@/redux/reducer/location";
import { useAppDispatch, useAppSelector } from "@/redux/hooks";
import { useRouter } from "next/router";
import { useEffect } from "react";
import { Spinner } from "@/components/Spinner/Spinner";

const Content = styled.div`
  display: flex;
  background: linear-gradient(160deg, #0f0f1a 0%, #1a1a2e 40%, #6c63ff 100%);
  min-height: 100vh;
  justify-content: center;
  align-items: center;
  width: 100%;
  padding: 2rem;
`;

const InputWrapper = styled.input(
  ({ theme: { color } }) => css`
    background-color: #f3f4f6;
    border-radius: 12px;
    font-size: 0.95rem;
    width: 100%;
    height: 3rem;
    padding: 0.75rem 1rem;
    border: 1.5px solid transparent;
    color: #1a1a2e;
    &::placeholder {
      color: #8e8e9a;
    }
    &:focus {
      outline: none;
      border-color: #6c63ff;
      box-shadow: 0 0 0 3px rgba(108, 99, 255, 0.12);
      background-color: #fff;
    }
  `,
);

export const LinkText = styled.a`
  text-decoration: none;
  color: #6c63ff;
  font-weight: 500;
  font-size: 0.9rem;
  margin-top: 0.5rem;
  &:hover {
    text-decoration: underline;
  }
`;

function Signin() {
  const data = useAppSelector(selectValue);
  const params = useAppSelector(paramsValue);
  const geo = useAppSelector(geoValue);
  const error = useAppSelector(errorValue);
  const loading = useAppSelector(loadingValue);
  const dispatch = useAppDispatch();
  const router = useRouter();

  useEffect(() => {
    if (data) {
      router.push("/map");
    }
  }, [data, router]);

  const formik = useFormik({
    initialValues: {
      email: "",
      password: "",
    },
    validationSchema: Yup.object({
      email: Yup.string()
        .email("Invalid email address")
        .required("Email is required"),
      password: Yup.string().required("Password is required"),
    }),
    onSubmit: async (values) => {
      dispatch(clearError());
      const result = await dispatch(signinuser(values));
      if (signinuser.fulfilled.match(result)) {
        const geoResult = await dispatch(geolocateUser());
        let searchParams = { ...params };
        let searchGeo = geo;
        if (geolocateUser.fulfilled.match(geoResult)) {
          const { city, state_code, lat, lng } = geoResult.payload;
          searchParams = { ...searchParams, city, state_code };
          searchGeo = { lat, lng };
        }
        await dispatch(
          getRealEstateData({
            type: searchParams.type || "sale",
            data: { ...searchParams, geo: searchGeo },
          }),
        );
      }
    },
  });

  return (
    <>
      <Head>
        <title>Sign In </title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <PageTemplate>
        <Content>
          <SignInWrapper>
            <HeaderSign>Welcome back</HeaderSign>
            <p
              style={{
                color: "#8E8E9A",
                fontSize: "0.9rem",
                margin: 0,
                textAlign: "center",
              }}
            >
              Sign in to continue to your account
            </p>
            <FormWrapper onSubmit={formik.handleSubmit}>
              <InputWrapper
                type="email"
                name="email"
                placeholder="Email address"
                value={formik.values.email}
                onChange={formik.handleChange}
                required
              />
              <InputWrapper
                type="password"
                name="password"
                placeholder="Password"
                value={formik.values.password}
                onChange={formik.handleChange}
                required
              />
              {error && (
                <div
                  style={{
                    color: "#E63946",
                    fontWeight: 600,
                    fontSize: "0.85rem",
                    textAlign: "center",
                    padding: "0.5rem",
                    background: "#FFF0F0",
                    borderRadius: "8px",
                    width: "100%",
                  }}
                >
                  {typeof error === "string" ? error : "Something went wrong"}
                </div>
              )}
              {
                <Button disabled={loading === "loading"}>
                  Sign In{" "}
                  {loading === "loading" ? (
                    <Spinner left="40%" top="20" />
                  ) : null}
                </Button>
              }

              <LinkText href="/signup">
                Don&apos;t have an account? Register &rarr;
              </LinkText>
            </FormWrapper>
          </SignInWrapper>
        </Content>
      </PageTemplate>
    </>
  );
}

export default Signin;
